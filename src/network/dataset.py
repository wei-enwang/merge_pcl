import torch
import json
import pickle
from torch.utils.data import Dataset
import numpy as np


class rgbd_data(Dataset):
    """
    Class for pybullet rgbd images. 

    Args:
        data_dir:
            directories of the saved images
        transform (a function or a list of functions):
            transform(s) applied to features (x)
        target_transform (a function or a list of functions):
            transform applied to label (y)
    """

    def __init__(self, data_paths, latent_paths, transform=None):

        self.data_paths = data_paths
        self.latent_paths = latent_paths
        self.transform = transform

    def __len__(self):
        assert len(self.data_paths) == len(self.latent_paths)
        return len(self.data_paths)

    def __getitem__(self, idx):
        img_path = self.data_paths[idx]
        latents = self.latent_paths[idx]
        img = np.load(img_path)

        # TODO: replace the following with the latents generated by occupancy network
        gt_latents_dict = json.load('../data/latents/trial_latents.json')
        gt_latents = []
        for l in pickle.load(latents):
            gt_latents.append(gt_latents_dict[l])

        return img, gt_latents

